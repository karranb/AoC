def check_direction(point, parsed_input, direction):
    i, j = direction
    x, y = point
    chars = ["M", "A", "S"]
    for char in chars:
        x += i
        y += j
        if x < 0 or y < 0 or x >= len(parsed_input) or y >= len(parsed_input[x]):
            return False
        if parsed_input[x][y] != char:
            return False
    return True


