def check_point(point, parsed_input):
    i, j = point
    if parsed_input[i][j] != "X":
        return 0
    count = 0
    # adjancents = []
    for item in adjancent_mapping:
        if check_direction(point, parsed_input, item):
            count += 1
    return count


