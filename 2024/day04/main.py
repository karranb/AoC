    def check_is_valid(point):
        x, y = point
        return x >= 0 and y >= 0 and x < len(parsed_input) and y < len(parsed_input[x])

    x, y = point
    if parsed_input[x][y] != "A":
        return 0
    for valid_mapping in valid_mappings:
        for item in valid_mapping:
            char, i, j = item
            new_x = x + i
            new_y = y + j
            if not check_is_valid((new_x, new_y)):
                break
